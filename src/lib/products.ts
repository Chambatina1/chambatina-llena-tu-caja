export type PackagingType = 'bag' | 'can' | 'box' | 'bottle' | 'jar' | 'bar' | 'pouch' | 'unit';

export interface Product {
  id: string;
  name: string;
  nameEs: string;
  category: string;
  weight: number;        // pounds (libras)
  price: number;          // USD - Walmart price
  width: number;          // inches (pulgadas)
  height: number;         // inches
  depth: number;          // inches
  volume: number;         // cubic inches (W × H × D)
  color: string;          // hex color for 3D visualization
  emoji: string;
  shelfLife: string;      // shelf life
  walmartUrl: string;     // product URL
  imageUrl: string;       // product image URL (populated at runtime)
  description: string;    // Spanish description
  packagingType: PackagingType;
}

export interface ProductInBox {
  product: Product;
  quantity: number;
  position: { x: number; y: number; z: number };
}

